namespace Catalog.Products.UpdateProduct;

public record UpdateProductRequest(
    Guid Id,
    string Name,
    string Description,
    decimal Price,
    List<string> Category,
    string ImageFile
);

public record UpdateProductResponse(bool Success);

public class UpdateProductEndpoint : ICarterModule
{
    public void AddRoutes(IEndpointRouteBuilder app)
    {
        app.MapPut(
                "/products",
                async (
                    UpdateProductRequest request,
                    ISender sender,
                    CancellationToken cancellationToken
                ) =>
                {
                    var command = request.Adapt<UpdateProductCommand>();

                    var result = await sender.Send(command, cancellationToken);

                    var response = result.Adapt<UpdateProductResponse>();

                    return Results.Ok(response);
                }
            )
            .WithTags("Products")
            .WithName("UpdateProduct")
            .WithSummary("Updates an existing product by ID.")
            .Produces<UpdateProductResponse>(StatusCodes.Status200OK)
            .ProducesProblem(StatusCodes.Status404NotFound)
            .ProducesProblem(StatusCodes.Status400BadRequest)
            .WithDescription("Updates the details of an existing product in the catalog.");
    }
}
